function convertirMayusculasYMinusculas() {

    var cadena = prompt("Escriba una cadena para combertirla en mayusculas y en minusculas:");
  
    var mayusculas = cadena.toUpperCase();
    var minusculas = cadena.toLowerCase();
  
  
    alert("Cadena en mayúsculas: " + mayusculas);
    alert("Cadena en minúsculas: " + minusculas);
  }
  
  convertirMayusculasYMinusculas();
  